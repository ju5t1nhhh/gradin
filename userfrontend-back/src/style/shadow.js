
//当鼠标滑过图片的时候 绑定一个事件 1.获取DOM对象 给她绑定事件
var inforshow=$(".infor-show");
//一共三个inforshow 鼠标滑过哪一个inforshow 如何给相同的类单独绑定一个事件

//第一行 第一个显示 其他隐藏
inforshow.eq(0).mouseover(function () {
    //滑过的时候 显示layer和description 1.获取这两个的DOM对象
    var layer=$(".layer");
    var description=$(".description");
    //2，改变他们的style display
    layer.eq(0).css("display","block");
    description.eq(0).css("display","block");
});
inforshow.eq(0).mouseleave(function () {
    //滑过的时候 显示layer和description 1.获取这两个的DOM对象
    var layer=$(".layer");
    var description=$(".description");
    //2，改变他们的style display
    layer.eq(0).css("display","none");
    description.eq(0).css("display","none");
});
